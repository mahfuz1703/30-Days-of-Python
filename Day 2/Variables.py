# In Python, a variable is a reserved memory location to store values. 
# In other words, a variable in a python program gives data to the computer for processing.

# Variable Naming Conventions:
# 1. A variable name must start with a letter or the underscore character.
# 2. A variable name cannot start with a number.
# 3. A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ ).
# 4. Variable names are case-sensitive (age, Age and AGE are three different variables).
# 5. The reserved words (keywords) cannot be used naming the variable.

# Examples of valid variable names:
my_variable = 10
_my_variable = 20
myVariable = 30
MYVARIABLE = 40
my_variable_1 = 50

# Examples of invalid variable names:
# 1myvariable = 10  # starts with a number
# my-variable = 20  # contains a hyphen
# my variable = 30  # contains a space